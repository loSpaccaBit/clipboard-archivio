# Remaining locales loaded from companion module to keep file maintainable
from locale_data import LOCALE_DATA  # noqa: E402

TRANSLATIONS.update(LOCALE_DATA)


def validate(data: dict[str, str], locale: str) -> list[str]:
    errors = []
    if len(data) != 140:
        errors.append(f"{locale}: expected 140 keys, got {len(data)}")
    missing = set(KEYS) - set(data)
    extra = set(data) - set(KEYS)
    if missing:
        errors.append(f"{locale}: missing {len(missing)} keys")
    if extra:
        errors.append(f"{locale}: extra {len(extra)} keys")
    for key in KEYS:
        en_specs = set(SPECIFIERS.findall(key))
        loc_specs = set(SPECIFIERS.findall(data.get(key, "")))
        if en_specs != loc_specs:
            errors.append(f"{locale}: specifier mismatch for {key[:40]}: {en_specs} vs {loc_specs}")
    return errors


def main() -> int:
    LOCALES_DIR.mkdir(parents=True, exist_ok=True)
    all_errors: list[str] = []
    summary: list[str] = []

    for locale, translations in sorted(TRANSLATIONS.items()):
        ordered = {k: translations[k] for k in KEYS}
        errs = validate(ordered, locale)
        all_errors.extend(errs)
        path = LOCALES_DIR / f"{locale}.json"
        with path.open("w", encoding="utf-8") as f:
            json.dump(ordered, f, ensure_ascii=False, indent=2)
            f.write("\n")
        summary.append(f"  {path.name}: {len(ordered)} keys")

    print("Created locale files:")
    print("\n".join(summary))
    if all_errors:
        print("\nValidation errors:")
        for e in all_errors:
            print(" ", e)
        return 1
    print("\n✓ All locale files valid (140 keys, format parity)")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())